import streamlit as st

st.title("Мини тест – География и История")

st.write("Отговорете на следните 5 въпроса:")

q1 = st.text_input("1. Коя е столицата на България?")

q2 = st.radio(
    "2. Коя е най-дългата река в България?",
    ["Марица", "Искър", "Дунав", "Струма"]
)

q3 = st.text_input("3. През коя година е основана българската държава?")

q4 = st.selectbox(
    "4. Кой е първият български цар?",
    ["Симеон I", "Аспарух", "Крум", "Борис I"]
)

q5 = st.text_input("5. На кой континент се намира България?")

if st.button("Провери отговорите"):
    score = 0

    if q1.strip().lower() == "софия":
        score += 1
    if q2 == "Дунав":
        score += 1
    if q3.strip() == "681":
        score += 1
    if q4 == "Аспарух":
        score += 1
    if q5.strip().lower() == "европа":
        score += 1

    st.write(f"Верни отговори: {score} / 5")

    if score == 5:
        st.success("Отлично! Всички отговори са верни!")
    elif score >= 3:
        st.info("Добър резултат, но можеш и по-добре.")
    else:
        st.error("Слаб резултат. Опитай отново.")

